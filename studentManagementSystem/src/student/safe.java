    public boolean check() {
        String newEmail = jTextField5.getText();
        String newPhone = jTextField6.getText();
        String oldemail = model.getValueAt(rowIndex, 4).toString();
        String oldphone = model.getValueAt(rowIndex, 5).toString();
        if (newEmail.equals(oldemail) && newPhone.equals(oldphone)) {
            return false;
        } else {
            if (!newEmail.equals(oldemail)) {
                boolean x = stdnt.isEmailExist(newEmail);
                if (x) {
                    JOptionPane.showConfirmDialog(this, "this email already exist");
                }
                return x;
            }
            if (!newPhone.equals(oldphone)) {
                boolean x = stdnt.isPhoneExist(newPhone);
                if (x) {
                    JOptionPane.showConfirmDialog(this, "this phone number already exist");
                }
                return x;
            }
        }
        return false;
    }
    
